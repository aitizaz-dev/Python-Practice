# Day 04 Operators in python
print("==== python operators ====")

a = 10
b = 30

print("===Arithmetic operators===")

print("Addition:",a+b)
print("Subtraction:",a-b)
print("Multiplication:",a*b)
print("Division:",a/b)
print("Floor division:",a//b)
print("Modulus:",a%b)
print("Power:",a**b)

print("===Comparision Operators===")

print("a==b:",a==b)
print("a!=b:",a!=b)
print("a>b:",a>b)
print("a<b:",a<b)
print("a>=b:",a>=b)
print("a<=b:",a<=b)

print("===Logical Operators===")

print((a>10)and(b<10))
print((a<20)or(b>10))
print(not(a>10))
